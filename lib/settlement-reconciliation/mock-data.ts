function seededAmount(orderNum: number): number {
  const base = 45 + (orderNum % 17) * 12.5
  return Math.round(base * 100) / 100
}

/**
 * ~150 clearing-account payments waiting to be matched against a Shopify payout.
 */
export function generateMockClearingPayments(): ClearingPayment[] {
  const payments: ClearingPayment[] = []

  for (let i = 1; i <= 150; i++) {
    const orderNum = 1000 + i
    const orderId = `#${orderNum}`
    let amount = seededAmount(i)

    // Seed two amount mismatches for demo review rows
    if (orderNum === 1091) amount = 142.5
    if (orderNum === 1092) amount = 198.75

    const day = 1 + (i % 14)
    const txnDate = `2026-02-${String(day).padStart(2, "0")}`

    payments.push({
      id: `clr-${orderNum}`,
      externalOrderId: orderId,
      amount,
      currency: "USD",
      txnDate,
      customerName: CUSTOMER_NAMES[i % CUSTOMER_NAMES.length]!,
      reference: `PMT-${orderNum}`,
    })
  }

  return payments
}

